# -------------------------
# 回答フォーム
# -------------------------
answers = []

with st.form("psychology_test"):
    for i, q in enumerate(questions):
        st.subheader(f"Q{i + 1}. {q['text']}")
        answer = st.radio(
            label="選択してください",
            options=list(q["options"].keys()),
            key=f"q{i}",
            index=None,
        )
        answers.append(answer)

    submitted = st.form_submit_button("診断する")

# -------------------------
# 採点処理
# -------------------------
if submitted:
    scores = {
        "協調性": 0,
        "外向性": 0,
        "開放性": 0,
        "誠実性": 0,
        "神経症的傾向": 0,
    }

    score_details = {
        "協調性": [],
        "外向性": [],
        "開放性": [],
        "誠実性": [],
        "神経症的傾向": [],
    }

    for i, (q, answer) in enumerate(zip(questions, answers)):
        point_data = q["options"][answer]

        for type_name, point in point_data.items():
            scores[type_name] += point

            score_details[type_name].append({
                "question_number": i + 1,
                "question_text": q["text"],
                "point": point
            })

    average_scores = {
        type_name: score / 2
        for type_name, score in scores.items()
    }

    st.session_state["scores"] = average_scores
    st.session_state["raw_scores"] = scores
    st.session_state["score_details"] = score_details
# -------------------------
# 結果表示
# -------------------------
if "scores" in st.session_state:
    st.divider()
    st.header("診断結果")

    scores = st.session_state["scores"]
    raw_scores = st.session_state["raw_scores"]
    score_details = st.session_state["score_details"]

    st.subheader("あなたのスコア")

    for type_name, score in scores.items():
        details = score_details[type_name]

        st.write(f"### {type_name}")

        for detail in details:
            st.write(
                f"Q{detail['question_number']}：{detail['point']}点"
            )

        points = [detail["point"] for detail in details]

        calculation = " + ".join(
            f"({p})" if p < 0 else str(p)
            for p in points
        )

        total = raw_scores[type_name]

        st.write(f"計算：{calculation} = {total}")
        st.write(f"平均：{total} ÷ 2 = {score} 点")